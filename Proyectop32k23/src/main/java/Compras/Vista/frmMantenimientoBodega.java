    public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("nombre");
        modelo.addColumn("Estatus");
        clsBodega bodega = new clsBodega();
        //VendedorDAO vendedorDAO = new VendedorDAO();
        List<clsBodega> listaBodega = bodega.getListadoBodega();
        tablaAplicaciones.setModel(modelo);
        String[] dato = new String[3];
        for (int i = 0; i < listaBodega.size(); i++) {
            dato[0] = Integer.toString(listaBodega.get(i).getid_bodega());
            dato[1] = listaBodega.get(i).getnombre_bodega();
            dato[2] = listaBodega.get(i).getdireccion_bodega();
            modelo.addRow(dato);
        }       
    }

